package day13whileloopdowhileloop;

public class DoWhileLoop01 {

    public static void main(String[] args) {

        //1)while-loop
        int i = 1;

        while (i<1) {
            System.out.println("Sen bir while loop'sun...");

            i++;
        }

        //2)do-while-loop
        int k = 1;

        do{
            System.out.println("Sen bir do while loop'sun...");
            k++;
        }while(k<1);

    }//main

}//class
